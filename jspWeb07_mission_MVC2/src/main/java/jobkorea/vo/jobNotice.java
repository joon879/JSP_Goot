package jobkorea.vo;

public class jobNotice {
	
	private String no;
	private String work;
	private String company;
	private String title;
	private String career;
	private String academic_abilty;
	private String prefer_basic;
	private String prefer_language;
	private String employment_type;
	private String salary;
	private String location;
	private String time;
	private String position;
	
	
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getWork() {
		return work;
	}
	public void setWork(String work) {
		this.work = work;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCareer() {
		return career;
	}
	public void setCareer(String career) {
		this.career = career;
	}
	public String getAcademic_abilty() {
		return academic_abilty;
	}
	public void setAcademic_abilty(String academic_abilty) {
		this.academic_abilty = academic_abilty;
	}
	public String getPrefer_basic() {
		return prefer_basic;
	}
	public void setPrefer_basic(String prefer_basic) {
		this.prefer_basic = prefer_basic;
	}
	public String getPrefer_language() {
		return prefer_language;
	}
	public void setPrefer_language(String prefer_language) {
		this.prefer_language = prefer_language;
	}
	public String getEmployment_type() {
		return employment_type;
	}
	public void setEmployment_type(String employment_type) {
		this.employment_type = employment_type;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
}
