package sjcafe.vo;

import java.util.Date;

public class SjCafe {
	
	private String sjno;
	private String sjtitle;
	private String sjwriter;
	private String sjcontent;
	private Date sjdate;
	private int sjhit;
	private int sjlike;
	private String sjfile;
	
	
	
	public String getSjno() {
		return sjno;
	}
	public void setSjno(String sjno) {
		this.sjno = sjno;
	}
	public String getSjtitle() {
		return sjtitle;
	}
	public void setSjtitle(String sjtitle) {
		this.sjtitle = sjtitle;
	}
	public String getSjwriter() {
		return sjwriter;
	}
	public void setSjwriter(String sjwriter) {
		this.sjwriter = sjwriter;
	}
	public String getSjcontent() {
		return sjcontent;
	}
	public void setSjcontent(String sjcontent) {
		this.sjcontent = sjcontent;
	}
	public Date getSjdate() {
		return sjdate;
	}
	public void setSjdate(Date sjdate) {
		this.sjdate = sjdate;
	}
	public int getSjhit() {
		return sjhit;
	}
	public void setSjhit(int sjhit) {
		this.sjhit = sjhit;
	}
	public int getSjlike() {
		return sjlike;
	}
	public void setSjlike(int sjlike) {
		this.sjlike = sjlike;
	}
	public String getSjfile() {
		return sjfile;
	}
	public void setSjfile(String sjfile) {
		this.sjfile = sjfile;
	}
	
	
	
}
